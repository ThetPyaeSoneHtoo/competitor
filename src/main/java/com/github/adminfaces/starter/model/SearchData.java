package com.github.adminfaces.starter.model;

/**
 *
 * @author Htet Nanda Kyaw
 */
public class SearchData {
    private String companyString;
    private String batchingPlantLocation;
    private String noOfBatchingPlants;
    private String zone;
    private String noOfTrucks;
    private String batchingCapacity;
    private String volumePerMonth;
    private String hdbApproved;
    private String tolDetail;
    private String supportedBy;
    private String capability;
    private String mapLocation;
    private String picture;
    private String legalIssues;

    public SearchData() {
    }

    public SearchData(String companyString, String batchingPlantLocation, String noOfBatchingPlants, String zone, String noOfTrucks, String batchingCapacity, String volumePerMonth, String hdbApproved, String tolDetail, String supportedBy, String capability, String mapLocation, String picture, String legalIssues) {
        this.companyString = companyString;
        this.batchingPlantLocation = batchingPlantLocation;
        this.noOfBatchingPlants = noOfBatchingPlants;
        this.zone = zone;
        this.noOfTrucks = noOfTrucks;
        this.batchingCapacity = batchingCapacity;
        this.volumePerMonth = volumePerMonth;
        this.hdbApproved = hdbApproved;
        this.tolDetail = tolDetail;
        this.supportedBy = supportedBy;
        this.capability = capability;
        this.mapLocation = mapLocation;
        this.picture = picture;
        this.legalIssues = legalIssues;
    }

    public String getCompanyString() {
        return companyString;
    }

    public void setCompanyString(String companyString) {
        this.companyString = companyString;
    }

    public String getBatchingPlantLocation() {
        return batchingPlantLocation;
    }

    public void setBatchingPlantLocation(String batchingPlantLocation) {
        this.batchingPlantLocation = batchingPlantLocation;
    }

    public String getNoOfBatchingPlants() {
        return noOfBatchingPlants;
    }

    public void setNoOfBatchingPlants(String noOfBatchingPlants) {
        this.noOfBatchingPlants = noOfBatchingPlants;
    }

    public String getZone() {
        return zone;
    }

    public void setZone(String zone) {
        this.zone = zone;
    }

    public String getNoOfTrucks() {
        return noOfTrucks;
    }

    public void setNoOfTrucks(String noOfTrucks) {
        this.noOfTrucks = noOfTrucks;
    }

    public String getBatchingCapacity() {
        return batchingCapacity;
    }

    public void setBatchingCapacity(String batchingCapacity) {
        this.batchingCapacity = batchingCapacity;
    }

    public String getVolumePerMonth() {
        return volumePerMonth;
    }

    public void setVolumePerMonth(String volumePerMonth) {
        this.volumePerMonth = volumePerMonth;
    }

    public String getHdbApproved() {
        return hdbApproved;
    }

    public void setHdbApproved(String hdbApproved) {
        this.hdbApproved = hdbApproved;
    }

    public String getTolDetail() {
        return tolDetail;
    }

    public void setTolDetail(String tolDetail) {
        this.tolDetail = tolDetail;
    }

    public String getSupportedBy() {
        return supportedBy;
    }

    public void setSupportedBy(String supportedBy) {
        this.supportedBy = supportedBy;
    }

    public String getCapability() {
        return capability;
    }

    public void setCapability(String capability) {
        this.capability = capability;
    }

    public String getMapLocation() {
        return mapLocation;
    }

    public void setMapLocation(String mapLocation) {
        this.mapLocation = mapLocation;
    }

    public String getPicture() {
        return picture;
    }

    public void setPicture(String picture) {
        this.picture = picture;
    }

    public String getLegalIssues() {
        return legalIssues;
    }

    public void setLegalIssues(String legalIssues) {
        this.legalIssues = legalIssues;
    }
    
}
